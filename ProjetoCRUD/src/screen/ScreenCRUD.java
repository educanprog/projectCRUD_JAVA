    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        // TODO add your handling code here:
        try {
            Project projeto = null;
            projeto = new Project(0,jTextFieldDescription.getText(), jTextFieldEndereco.getText(), jTextFieldPrice.getText());
            IProjectDAO consultorBD= null;
            consultorBD = new ProjectDAO();
            consultorBD.createProject(projeto);
            limparTela();
            atualizarTable(consultorBD.listaDeProjeto());
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage());
        }
        }
        private void limparTela(){
            jTextFieldDescription.setText("");
            jTextFieldEndereco.setText("");
            jTextFieldPrice.setText("");
            jTextFieldId.setText("");
        }
        private void atualizarTable(ArrayList<Project> listaDeProjeto){
            try {
                DefaultTableModel model = (DefaultTableModel) jTableDados.getModel();
                model.setNumRows(0);
                for (int pos = 0; pos < listaDeProjeto.size(); pos++) {
                    Project project = listaDeProjeto.get(pos);
                    String[] linha = new String[4];
                    linha[0] = project.getIdproject() + "";
                    linha[1] = project.getDescription();
                    linha[2] = project.getEndereco();
                    linha[3] = project.getPrice();
                    model.addRow(linha);
                }
                //JOptionPane.showMessageDialog(rootPane, dadosDosProfessores.toString());

            } catch (Exception erro) {
                JOptionPane.showMessageDialog(rootPane, erro.getMessage());
            }
    }//GEN-LAST:event_jButtonIncluirActionPerformed

